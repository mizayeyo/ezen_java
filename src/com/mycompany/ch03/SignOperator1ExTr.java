package com.mycompany.ch03;

public class SignOperator1ExTr {

	public static void main(String[] args) {


		int x = 100;
		
		int a = +x;
		int b = -x;
		
		System.out.println("a: " + a);
		System.out.println("b: " + b);
		
		
		int k = 5;
		
		int c = +k;
		int d = -k;
		
		System.out.println("c: " + c);
		System.out.println("d: " + d);
				

	}

}
